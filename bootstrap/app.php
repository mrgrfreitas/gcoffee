<?php

use Dotenv\Dotenv;
$dotEnv = Dotenv::createMutable(dirname(__DIR__));
$dotEnv->load();

/*
|--------------------------------------------------------------------------
| Create The Application
|--------------------------------------------------------------------------
*/

$app = new \Machine\App\Application(
    $_ENV['APP_ROOT_PATH'] ?? dirname(__DIR__)
);


/*
|--------------------------------------------------------------------------
| Get the Routes
|--------------------------------------------------------------------------
*/
require_once __DIR__ . "./../routes/web.php";

/*
|--------------------------------------------------------------------------
| Run The Application
|--------------------------------------------------------------------------
*/
$app->run();